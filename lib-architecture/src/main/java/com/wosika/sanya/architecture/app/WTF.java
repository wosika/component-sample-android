package com.wosika.sanya.architecture.app;

public class WTF {

    private String name;
    private String age;

    public WTF(String name, String age) {
        this.name = name;
        this.age = age;
    }


    public String getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
